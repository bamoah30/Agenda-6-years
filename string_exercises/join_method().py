words = ["Hello", "from", "Bernard"]
sentence = " ".join(words)
print(sentence)  # Output: Hello from Bernard


print("-".join(["2024", "06", "15"]))  # Output: 2024-06-15 
print("".join(["A", "B", "C", "D"]))  # Output: ABCD    
print(" and ".join(["Tea", "Coffee", "Milk"]))  # Output: Tea and Coffee and Milk   
print(", ".join(["Apple", "Banana", "Cherry"]))  # Output: Apple, Banana, Cherry
print("".join(["This", "is", "a", "test"]))  # Output: Thisisatest
print("->".join(["Path", "to", "file"]))  # Output: Path->to->file